# Randomniye chisla
x = 9
y = 10
# Sravneniye
a = x >= y

# Opat randomniye chisla
s = 23
p = 35
# Sravneniye
b = s >= p

# AND
if a and b: # yesli a i b
    print('All values are TRUE')
else: # v protivnom sluchaye
    print('All values are FALSE')

# OR
if a or b: # yesli a ili b
    print('One or both the values is TRUE')
else:
    print('Both values are FALSE')